function createTextStyle(feature, resolution, labelText, labelFont, labelFill, placement, bufferColor, bufferWidth) {
    var textStyle = new ol.style.Text({
        font: labelFont,
        text: labelText,
        textAlign: 'center',
        textBaseline: 'middle',
        fill: new ol.style.Fill({
            color: labelFill
        }),
        placement: placement
    });
    
    if (bufferWidth > 0) {
        textStyle.setStroke(new ol.style.Stroke({
            color: bufferColor,
            width: bufferWidth
        }));
    }
    
    return textStyle;
}
